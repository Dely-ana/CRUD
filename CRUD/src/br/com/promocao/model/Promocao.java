package br.com.promocao.model;

public class Promocao {
	
		private	int id ;
		private String promocao_relampago ;
		private String passagens;
		private String pacotes;
		private String hospedagem;
		



		public int getId() {
			return id;
		}
		public void setId(int id) {
			this.id = id;
		}
		public String getPromocao_relampago() {
			return promocao_relampago;
		}
		public void setPromocao_relampago(String promocao_relampago) {
			this.promocao_relampago = promocao_relampago;
		}
		public String getPassagens() {
			return passagens;
		}
		public void setPassagens(String passagens) {
			this.passagens = passagens;
		}
		public String getPacotes() {
			return pacotes;
		}
		public void setPacotes(String pacotes) {
			this.pacotes = pacotes;
		}
		public String getHospedagem() {
			return hospedagem;
		}
		public void setHospedagem(String hospedagem) {
			this.hospedagem = hospedagem;
		}

}		